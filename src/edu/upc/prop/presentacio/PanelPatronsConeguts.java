    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        List<String> llista = new ArrayList<String>();
//        for (int i = 0; i < jListPacients.getModel().getSize(); i++) {
//            llista.add((String) jListPacients.getModel().getElementAt(i));
//        }
        for (int i = 0; i < modelPacients.size(); i++) {
            String pacient = (String) modelPacients.get(i);
            llista.add(pacient.substring(0, pacient.indexOf(":")).trim());
        }
        controladorEstudis.setDnis(llista);
        llista.clear();

//        for (int i = 0; i < jListMalalties.getModel().getSize(); i++) {
//            llista.add((String) jListMalalties.getModel().getElementAt(i));
//        }
        for (int i = 0; i < modelMalalties.size(); i++) {
            llista.add((String) modelMalalties.get(i));
        }
        controladorEstudis.setMalalties(llista);
        llista.clear();

//        for (int i = 0; i < jListSimptomes.getModel().getSize(); i++) {
//            llista.add((String) jListSimptomes.getModel().getElementAt(i));
//        }
        for (int i = 0; i < modelSimptomes.size(); i++) {
            llista.add((String) modelSimptomes.get(i));
        }
        controladorEstudis.setSimptomes(llista);
        llista.clear();

//        for (int i = 0; i < jListPatrons.getModel().getSize(); i++) {
//            llista.add((String) jListPatrons.getModel().getElementAt(i));
//        }
        for (int i = 0; i < modelPatrons.size(); i++) {
            String patro = (String) modelPatrons.get(i);
            llista.add(patro.substring(patro.indexOf(":") + 1).trim());
        }
        controladorEstudis.setPatronsConeguts(llista);

        if (!jTextFieldDNIFi.getText().isEmpty()) {
            if (jTextFieldDNIFi.getText().length() != 8) {
                jLabelFormat.setText("El Dni ha de tenir 8 dígits");
                return;
            }
            controladorEstudis.setDniFinal(jTextFieldDNIFi.getText());
        } else {
            controladorEstudis.setDniFinal(null);
        }

        if (!jTextFieldDNIInici.getText().isEmpty()) {
            if (jTextFieldDNIInici.getText().length() != 8) {
                jLabelFormat.setText("El Dni ha de tenir 8 dígits");
                return;
            }
            controladorEstudis.setDniInici(jTextFieldDNIInici.getText());
        } else {
            controladorEstudis.setDniInici(null);
        }

        if (!jTextFieldEdatFi.getText().isEmpty()) {
            controladorEstudis.setEdatFi(Integer.parseInt(jTextFieldEdatFi.getText()));
        } else {
            controladorEstudis.setEdatFi(-1);
        }

        if (!jTextFieldEdatInici.getText().isEmpty()) {
            controladorEstudis.setEdatInici(Integer.parseInt(jTextFieldEdatInici.getText()));
        } else {
            controladorEstudis.setEdatInici(-1);
        }

        if (jRadioButtonFemeni.isSelected()) {
            controladorEstudis.setSexe('F');
        } else if (jRadioButtonMasculi.isSelected()) {
            controladorEstudis.setSexe('M');
        } else {
            controladorEstudis.setSexe('X');
        }

        if (!jTextFieldQ.getText().isEmpty()) {
            controladorEstudis.setQ(Integer.parseInt(jTextFieldQ.getText()));
        } else {
            controladorEstudis.setQ(-1);
        }

        if (!jTextFieldError.getText().isEmpty()) {
            controladorEstudis.setMargeError(Double.parseDouble(jTextFieldError.getText()) / 100.0);
        } else {
            controladorEstudis.setMargeError(0.01);
        }

        if (!jTextFieldLongMax.getText().isEmpty()) {
            controladorEstudis.setLongMax(Integer.parseInt(jTextFieldLongMax.getText()));
        } else {
            controladorEstudis.setLongMax( Integer.MAX_VALUE / 1000);
        }

        if (!jTextFieldLongMin.getText().isEmpty()) {
            controladorEstudis.setLongMin(Integer.parseInt(jTextFieldLongMin.getText()));
        } else {
            controladorEstudis.setLongMin(0);
        }

        ponerGif();
        disableTot();
        controladorEstudis.buscarPatronsExistents(jCheckBoxParalelo.isSelected(), jCheckBoxinsertions.isSelected());
    }//GEN-LAST:event_jButton1ActionPerformed

