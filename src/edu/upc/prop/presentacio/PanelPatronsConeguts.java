    private void intInputVerifier(String anterior, java.awt.event.KeyEvent evt) {
        char key = evt.getKeyChar();
        try {
            Integer.parseInt(anterior + key);
        } catch (Exception e) {
            evt.setKeyChar('\0');
        }
    }

    private void doubleInputVerifier(String anterior, java.awt.event.KeyEvent evt) {
        char key = evt.getKeyChar();
        try {
            Double.parseDouble(anterior + key);
        } catch (Exception e) {
            evt.setKeyChar('\0');
        }
    }
